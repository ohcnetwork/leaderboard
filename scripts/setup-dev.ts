/**
 * Build the dummy plugin
 */
async function buildDummyPlugin(): Promise<void> {
  console.log("🔨 Building dummy plugin...");

  try {
    execSync("pnpm --filter @leaderboard/plugin-dummy build", {
      stdio: "inherit",
    });
    console.log("   ✓ Plugin built successfully\n");
  } catch (error) {
    console.error("\n❌ Failed to build plugin");
    throw error;
  }
}

