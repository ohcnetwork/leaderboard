/**
 * Main setup function
 */
async function main() {
  console.log("\n🎯 Leaderboard Development Setup\n");

  const options = parseArgs();

  // Check if data directory exists
  const exists = await checkDataDir();

  if (exists && !options.force) {
    console.log("⚠️  Data directory already exists!");
    console.log("   Use --force to overwrite or run 'pnpm clean:dev' first.\n");
    process.exit(1);
  }

  if (exists && options.force) {
    console.log("🗑️  Removing existing data directory...");
    await fs.rm(DATA_DIR, { recursive: true, force: true });
    console.log("   ✓ Removed\n");
  }

  try {
    // Create directory structure
    await createDirStructure();

    // Generate config
    await generateConfig(options);

    // Create empty theme.css as placeholder
    await createThemeFile();

    // Build plugin
    await buildDummyPlugin();

    // Run plugin runner
    await runPluginRunner();

    // Display success message
    displaySuccess(options);
  } catch (error) {
    console.error("\n❌ Setup failed:", error);
    process.exit(1);
  }
}

// Run the script
main().catch((error) => {
  console.error("Fatal error:", error);
  process.exit(1);
});
